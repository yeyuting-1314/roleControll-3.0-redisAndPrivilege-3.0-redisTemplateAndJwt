package select.system.dto;

/**
 * @author yeyuting
 * @create 2021/1/27
 */
public class Role {
    int id ;
    String roleName ;
    String role ;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getRoleName() {
        return roleName;
    }

    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
